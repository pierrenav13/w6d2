class SQLObject
  def self.columns
    return @columns unless @columns.nil?
    array = DBConnection.execute2(<<-SQL).first
      SELECT
        *
      FROM
        #{self.table_name}
      LIMIT 0
    SQL
    array.map!{|ele| ele.to_sym}
    @columns = array
  end

  def self.finalize!
    self.columns.each do |col|
      define_method(col) do
        self.attributes[col]
      end

      define_method("#{col}=") do |value|
        self.attributes[col] = value
      end
    end
  end

  def self.table_name=(table_name)
    @table_name = table_name
  end

  def self.table_name
    t = self.to_s.tableize
    return t unless t[-1] != 's'
    self.to_s.downcase + 's'
  end

